/* Structured Query Language - SQL

SQL é divido em quatro partes:
- DML: Data Manipulation Language (comandos do tipo: INSERT, SELECT, UPDATE, DELETE) <- CRUD (Create, Read, Update, Delete)
- DDL: Data Definition Language (comandos do tipo: CREATE, ALTER, DROP) <- Definição do esquema do banco de dados
- DCL: Data Control Language (comandos do tipo: GRANT, DENY, REVOKE) <- Garante ou revoga acessos
- TCL: Transaction Control Language (comandos do tipo: BEGIN TRANS, COMMIT TRANS, ROLLBACK) <- Gerencia transações dentro de um banco de dados*/