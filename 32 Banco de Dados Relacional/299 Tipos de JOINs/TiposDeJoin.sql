-- Tipos de Join
/* 
- Inner Join: é a interseção de dados de duas tabelas. Esse join apenas retornará valores que
possuem alguma relação de chave primária de uma tabela com uma chave estrangeira de outra.

- Outer Join: oposto do Inner Join. Retorna tudo o que não está relacionado entre duas tabelas.

- Left Outer Join: traz todos os resultados de uma tabela A assim como o Inner entre A e B.

- Right Outer Join: traz todos os resultados de uma tabela B assim como o Inner entre B e A.

- Full Join (não suportado pelo SQL): união de Left Outer Join com Right Outer Join.
*/