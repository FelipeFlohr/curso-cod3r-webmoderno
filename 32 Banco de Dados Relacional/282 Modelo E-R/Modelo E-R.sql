-- Modelo Entidade/Relacionamento ou Modelo E/R

-- Os dados dentro de um banco de dados são armazenados em tabelas.
-- As colunas são os atributos de um determinado dado e as linhas são os dados.

-- Dentro de um modelo relacionado, os elementos precisam estar condizentes ao esquema,
-- isto é: os elementos precisam estar corretamente colocados em suas colunas. Usando
-- um exemplo em banco não-relacionado, uma entidade "Carro" poderá ter um ou mais atri-
-- butos em branco, enquanto num banco relacionado, todos os atributos deverão estar
-- preenchidos.

-- Outra característica importante do modelo E/R é o relacionamento em si. Classes podem estar
-- relacionadas umas com as outras. Exemplo: em um site de vendas, um cliente comprará três
-- produtos: um vídeo-game e 2 jogos. Com isso, pode-se dizer que a tabela "Cliente" e a tabela
-- "Produto" estarão relacionadas com uma tabela "Pedido".
